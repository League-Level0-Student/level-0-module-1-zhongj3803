package _05_for_loops._2_badgers;

public class Badger {
public static void main(String[] args) {
	for (int i=0; i<11; i++) {
		System.out.println("badger");
	}
	
	for (int i=0; i<2; i++) {
		System.out.println("mushroom");
	}
	for (int i=0; i<11; i++) {
		System.out.println("badger");
	}
	
	for (int i=0; i<2; i++) {
		System.out.println("mushroom");
	}
	System.out.println("A snake!");
}
}
